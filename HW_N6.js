// Задача 3: Таблица умножения
// ================================================

// Описание: 
// Вывести таблицу умножения для чисел от 2 до 9. 
// Каждое число умножать на числа от 1 до 10.

function task3MultiplicationTable() {
    console.log("=== Задача 3: Таблица умножения ===");

    // Внешний цикл: числа от 2 до 9
    for (let i = 2; i <= 9; i++) {
        console.log(`Таблица умножения для ${i}:`);
        for (let j = 1; j <= 10; j++) {
            console.log(`${i} x ${j} = ${i * j}`);
        }
        console.log("--------------------------------------------");
    }

    alert("Таблица умножения выведена в консоль.");
}
// Задача 3: Генерирует таблицу умножения с использованием вложенных циклов for.


