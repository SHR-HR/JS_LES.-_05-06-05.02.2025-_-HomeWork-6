// ================================================
// Задача 1: Зациклить калькулятор
// ================================================

// Описание: 
// Пользователь вводит два числа и знак операции. 
// Необходимо выполнять расчеты до тех пор, пока пользователь не откажется.

function task1Calculator() {
    console.log("=== Задача 1: Зацикленный калькулятор ===");
    let continueCalculation = true;

    while (continueCalculation) {
        // Запрашиваем у пользователя данные
        let num1 = prompt("Введите первое число:");
        let num2 = prompt("Введите второе число:");
        const operator = prompt("Введите знак операции (+, -, *, /):");

        // Проверяем нажатие "Отмена" в prompt
        if (num1 === null || num2 === null || operator === null) {
            alert("Операция отменена пользователем.");
            return;
        }

        num1 = parseFloat(num1.trim());
        num2 = parseFloat(num2.trim());

        // Проверяем корректность ввода чисел
        if (isNaN(num1) || isNaN(num2)) {
            alert("Ошибка! Введите корректные числа.");
            continue;
        }

        // Проверяем и выполняем операцию
        let result;
        switch (operator) {
            case "+":
                result = num1 + num2;
                break;
            case "-":
                result = num1 - num2;
                break;
            case "*":
                result = num1 * num2;
                break;
            case "/":
                if (num2 === 0) {
                    alert("Ошибка! Деление на ноль невозможно.");
                    continue;
                }
                result = num1 / num2;
                break;
            default:
                alert("Ошибка! Некорректный оператор. Используйте только +, -, *, /.");
                continue;
        }

        // Вывод результата
        console.log(`Результат: ${num1} ${operator} ${num2} = ${result}`);
        alert(`Результат: ${num1} ${operator} ${num2} = ${result}`);

        // Спрашиваем, хочет ли пользователь продолжить
        continueCalculation = confirm("Хотите выполнить еще одну операцию?  Тогда, нажмите ОК либо ДА, чтобы выполнить еще одну операцию. Нажмите Отмена либо НЕТ, чтобы завершить работу с калькулятором.");
    }
}
// Задача 1: Обрабатывает ввод пользователя, использует switch для выполнения операций и confirm для запроса на продолжение.


// ================================================
// Задача 2: Вывод дней недели
// ================================================

// Описание: 
// Выводить дни недели в цикле. 
// Каждый раз спрашивать пользователя, хочет ли он увидеть следующий день.

function task2DaysOfWeek() {
    console.log("=== Задача 2: Вывод дней недели ===");
    const days = ["Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье"];
    let i = 0;

    do {
        alert(`День недели: ${days[i]}`);
        console.log(`День недели: ${days[i]}`);
        i = (i + 1) % days.length; // Переходим к следующему дню
    } while (confirm("Нажмите ОК либо ДА, чтобы увидеть следующий день. Нажмите Отмена либо НЕТ, чтобы выйти."));
}
// Задача 2: Использует массив дней недели и цикл do...while для бесконечного вывода, пока пользователь нажимает "OK".


